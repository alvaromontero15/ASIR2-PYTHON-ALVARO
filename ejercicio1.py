#Calcular el cuadrado de un número

numero= int(input("Escriba el número deseado para calcular su cuadrado: ")) #Pedir al usuario que ingrese un número
resultado = numero * numero #Calcular el cuadrado del número
solucion = resultado #Almacenar el resultado en una variable
print("El cuadrado de", numero, "es", solucion) #Mostrar el resultado al usuario